void main() {
  /**
   * Difference between var and dynamic
   * var:- var variable once assigned,the type can not change
   * dynamic:- Dynamic variable type can change over time.
   * 
   * 
   */
  // they change
  dynamic a = 10;
  print(a);
  a = 5.5;
  print(a);
  a = 'Shiv';
  print(a);
}
